#include <stdio.h>

int main() {
    // ใช้ for loop แสดงเลขคู่ 2-10
    for (int i = 2; i <= 10; i += 2) {
        printf("%d ", i);
    }
    printf("\nEnd of loop\n");

    // อธิบาย: for loop จะกำหนดค่าเริ่มต้น เงื่อนไข และการเพิ่มค่าในบรรทัดเดียวกัน ทำงานจนกว่า i จะมากกว่า 10

    return 0;
}

/*
ใช้ for loop กำหนดค่าเริ่มต้น i = 2 เงื่อนไข i <= 10 และเพิ่มค่า i ทีละ 2 
ในบรรทัดเดียวกัน ลูปจะทำงานโดยพิมพ์ค่า i ทุกครั้งที่เงื่อนไขเป็นจริง 
จนกว่า i จะมากกว่า 10 จึงออกจากลูปและแสดงข้อความ "End of loop"
*/

