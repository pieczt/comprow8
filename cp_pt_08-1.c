#include <stdio.h>

int main() {
    // ใช้ while loop แสดงเลขคู่ 2-10
    int i = 2;
    while (i <= 10) {
        printf("%d ", i);
        i += 2;
    }
    printf("\nEnd of loop\n");

    // อธิบาย: while loop จะตรวจสอบเงื่อนไขก่อนทุกครั้ง ถ้า i ยังไม่เกิน 10 จะพิมพ์ค่า i แล้วเพิ่มทีละ 2

    return 0;
}

/*
เริ่มต้นที่ค่า i = 2 แล้วใช้ while loop ตรวจสอบเงื่อนไขว่า i <= 10 
ถ้าเงื่อนไขเป็นจริงจะพิมพ์ค่า i และเพิ่มค่า i ทีละ 2 
ทำซ้ำจนกว่า i จะมากกว่า 10 จึงออกจากลูปและแสดงข้อความ "End of loop"
*/